
class Exceptions:
    def __init__(self) -> None:
        pass

    def ValueError(self) -> ValueError:
        raise('ValueError: The value is not correct')
        
    
    def TypeError(self) -> TypeError:
        raise('TypeError: The type is not correct')
    
    def IndexError(self) -> IndexError:
        raise('IndexError: The index is not correct')
        
    
    